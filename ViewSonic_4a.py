from itertools import permutations
import random
def prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True


def generate():

    select = random.sample(range(10), 5)  
    nums = list(permutations(select, 5))
    numbers = []
    for i in nums:
        s = ''
        for j in range(5):
            s += str(i[j])
        numbers.append(s)

    return numbers


